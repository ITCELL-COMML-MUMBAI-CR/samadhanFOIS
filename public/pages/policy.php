document.addEventListener('DOMContentLoaded', function() {
    // Smooth scrolling for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
    
    // Highlight current section in navigation
    const sections = document.querySelectorAll('[id^="section-"]');
    const navLinks = document.querySelectorAll('a[href^="#section-"]');
    
    window.addEventListener('scroll', function() {
        let current = '';
        sections.forEach(section => {
            const sectionTop = section.offsetTop - 100;
            if (pageYOffset >= sectionTop) {
                current = section.getAttribute('id');
            }
        });
        
        navLinks.forEach(link => {
            link.classList.remove('fw-bold', 'text-primary');
            if (link.getAttribute('href') === '#' + current) {
                link.classList.add('fw-bold', 'text-primary');
            }
        });
    });
});

function sharePolicy() {
    if (navigator.share) {
        navigator.share({
            title: 'SAMPARK - General Policy',
            text: 'Terms, Conditions & Guidelines for Central Railway Freight Grievance System',
            url: window.location.href
        });
    } else {
        // Fallback: copy URL to clipboard
        navigator.clipboard.writeText(window.location.href).then(() => {
            SAMPARKApp.alerts.success('Policy URL copied to clipboard!');
        });
    }
}
</script>

